    public void ServerPlay(int row, int col) {
        
        Boolean draw = JogoDaVelhaMain.estaCheio(JogoDaVelhaMain.arr);

        if (draw == true) {
            JOptionPane.showMessageDialog(null, "Empate, voc칡 conseguiu perder para um computador 游", "Aviso", JOptionPane.WARNING_MESSAGE);
        }

    
        try {
            // o cliente est치 pedindo conex칚o com o servidor que est치 no IP e Porta informados
            socket = new Socket(Client.Client.ip, Client.Client.porta);
            System.out.println("Conectado com o servidor.");
//            jLabel1.setText("Vez do Computador");
        } catch (Exception e) {
            System.out.println("Erro: " + e.getMessage());
            return;
        }
        
        try {
            
            Thread.sleep(3000);  
            out = new ObjectOutputStream(socket.getOutputStream());
            in = new ObjectInputStream(socket.getInputStream());
            int valor2 = 1;
            Req request = new Req(valor2, row, col, false);

            out.writeObject(request);   
            
            Resp response = (Resp) in.readObject();
            
            jogodavelha.JogoDaVelhaMain.arr.get(response.getRowIndex()).set(response.getColIndex(), response.getPcOperator());
            // DEBUG: Servidor checa o tabuleiro.
            jogodavelha.JogoDaVelhaMain.checkBoard1x1();

                        
            if (response.getRowIndex() == 0 && response.getColIndex() == 0) {
                jButton1.setText("O");
            } else if (response.getRowIndex() == 0 && response.getColIndex() == 1) {
                jButton2.setText("O");
            } else if (response.getRowIndex() == 0 && response.getColIndex() == 2) {
                jButton3.setText("O");
            } else if (response.getRowIndex() == 1 && response.getColIndex() == 0) {
                jButton4.setText("O");
            } else if (response.getRowIndex() == 1 && response.getColIndex() == 1) {
                jButton5.setText("O");
            } else if (response.getRowIndex() == 1 && response.getColIndex() == 2) {
                jButton6.setText("O");
            } else if (response.getRowIndex() == 2 && response.getColIndex() == 0) {
                jButton7.setText("O");
            } else if (response.getRowIndex() == 2 && response.getColIndex() == 1) {
                jButton8.setText("O");
            } else if (response.getRowIndex() == 2 && response.getColIndex() == 2) {
                jButton9.setText("O");
            }

            System.out.println("Response" + response.getRowIndex() + response.getColIndex());

        } catch (Exception e) {
            System.out.println("Erro na comunica칞칚o: " + e.getMessage());
        }
    }
    
